"""
Thermal-Hydro-Mechanical (THM) coupled problem in a 2-D saturated porous medium.

Models the coupled response of a geomaterial under thermal and hydraulic loading:
  - Heat conduction (Fourier's law)
  - Biot consolidation (pore-pressure dissipation + thermal pressurisation)
  - Linear quasi-static poroelastic deformation

State variables (5 processes on a size×size grid):
    T    - temperature field              [K, normalised]
    p    - pore fluid pressure            [Pa, normalised]
    ux   - horizontal displacement        [m, normalised]
    uy   - vertical displacement          [m, normalised]
    ev   - volumetric strain ∂ux/∂x+∂uy/∂y

Governing equations (dimensionless, plane-strain):
    Heat:       ρc ∂T/∂t  =  κ_T ∇²T  +  Q_T(x,y)          [parabolic]
    Hydraulic:  Ss ∂p/∂t  =  c_h ∇²p  +  L_T (T-T_old)/dt  [parabolic]
    Mechanical: ∇²ux = ∂εv/∂x,   ∇²uy = ∂εv/∂y             [elliptic, simplified]
    Volumetric: εv   = (α p + E_T T) / K_drained            [algebraic]

where:
    c_h  = k/(μ Ss)   hydraulic diffusivity
    L_T  = β_T        thermo-hydraulic coupling (thermal pressurisation)
    E_T  = 3 K_s α_T  thermo-mechanical coupling
    α    = Biot coefficient

The mechanical model uses the irrotational (curl-free) approximation, which
decouples ux and uy into two independent Poisson problems — a valid
simplification for problems driven by smooth body-force-like loadings.

Numerical scheme: operator splitting per macro time step dt_eval
    1. Advance T  (backward Euler, pre-factored sparse system)
    2. Advance p  (backward Euler, pre-factored sparse system)
    3. Compute εv (algebraic)
    4. Solve ux, uy (Poisson, shared pre-factored matrix)

Pre-factoring the sparse systems once per environment gives O(N) per-step cost.

Different environments → different material properties (stiffness, permeability).
Different trajectories → different smooth random initial T and p distributions.

Output format: [N_traj, N_env, 5, T, size, size]
    channel 0: T   (temperature)
    channel 1: p   (pore pressure)
    channel 2: ux  (x-displacement)
    channel 3: uy  (y-displacement)
    channel 4: ev  (volumetric strain)

References
----------
Gao, Q. et al. (2020). A coupled THM model for saturated porous rock, IJRMMS.
Coussy, O. (2004). Poromechanics. Wiley.
Biot, M. A. (1941). General theory of three-dimensional consolidation, JAP.
"""

import numpy as np
import torch
from torch.utils.data import Dataset
from scipy.sparse import diags, eye, kron, csr_matrix
from scipy.sparse.linalg import factorized


class THMDataset(Dataset):
    """
    THM dataset generated by operator-splitting with pre-factored sparse solvers.

    Args:
        num_traj_per_env: number of trajectories (initial conditions) per environment
        size:             spatial grid size (default 64 → 64×64 grid)
        time_horizon:     total dimensionless simulation time
        dt_eval:          time interval between saved snapshots
        params:           list of dicts with keys:
                          {rho_c, kappa_T, Ss, c_h, L_T, alpha, E_T, K_drained, G}
        group:            'train' or 'test'
    """

    DEFAULT_PARAMS = [
        # Env 0: soft, high-permeability rock (fast drainage, moderate coupling)
        {
            "rho_c":    1.0,    # volumetric heat capacity (normalised)
            "kappa_T":  0.10,   # thermal conductivity
            "Ss":       1.0,    # specific storage
            "c_h":      0.05,   # hydraulic diffusivity k/(μ Ss)
            "L_T":      0.30,   # thermo-hydraulic coupling β_T
            "alpha":    0.80,   # Biot coefficient
            "E_T":      0.20,   # thermo-mechanical coupling 3 K_s α_T
            "K_drained":1.0,    # drained bulk modulus (normalised)
            "G":        1.0,    # shear modulus (for Poisson solve)
        },
        # Env 1: medium stiffness, moderate permeability
        {
            "rho_c":    1.0,
            "kappa_T":  0.08,
            "Ss":       0.8,
            "c_h":      0.02,
            "L_T":      0.40,
            "alpha":    0.70,
            "E_T":      0.25,
            "K_drained":1.5,
            "G":        1.2,
        },
        # Env 2: stiff, low-permeability rock (slow drainage, strong coupling)
        {
            "rho_c":    1.0,
            "kappa_T":  0.06,
            "Ss":       0.6,
            "c_h":      0.008,
            "L_T":      0.50,
            "alpha":    0.60,
            "E_T":      0.30,
            "K_drained":2.0,
            "G":        1.5,
        },
    ]

    def __init__(
        self,
        num_traj_per_env: int,
        size: int = 64,
        time_horizon: float = 1.0,
        dt_eval: float = 0.1,
        params: list = None,
        group: str = "train",
    ):
        super().__init__()
        self.num_traj_per_env = num_traj_per_env
        self.params_eq = params if params is not None else self.DEFAULT_PARAMS
        self.num_env   = len(self.params_eq)
        self.len       = num_traj_per_env * self.num_env
        self.size      = size
        self.N         = size * size
        self.time_horizon = float(time_horizon)
        self.dt_eval   = dt_eval
        self.n_steps   = int(round(time_horizon / dt_eval))
        self.test      = group == "test"
        self.max_seed  = np.iinfo(np.int32).max
        self.buffer    = {}
        self.indices   = [
            list(range(e * num_traj_per_env, (e + 1) * num_traj_per_env))
            for e in range(self.num_env)
        ]
        self.dx = 1.0 / (size - 1)   # grid spacing on [0,1]²

        # Interior grid (Dirichlet BC: all fields = 0 on boundary)
        self.n_int = size - 2         # interior points per direction
        self.N_int = self.n_int ** 2  # total interior unknowns

        # Pre-build and factor all sparse systems (once per environment)
        self._T_solve  = []   # factorised heat operator
        self._p_solve  = []   # factorised hydraulic operator
        self._u_solve  = []   # factorised mechanical Poisson operator (shared ux,uy)
        self._L_int    = None  # interior Laplacian (computed once)

        L = self._build_interior_laplacian()
        self._L_int = L
        for e, pr in enumerate(self.params_eq):
            dt = self.dt_eval
            # Heat:       (ρc/dt I - κ_T L) T = rhs
            A_T = (pr["rho_c"] / dt) * eye(self.N_int, format="csr") - pr["kappa_T"] * L
            # Hydraulic:  (Ss/dt I - c_h  L) p = rhs
            A_p = (pr["Ss"]    / dt) * eye(self.N_int, format="csr") - pr["c_h"]     * L
            # Mechanical: G * L * u = rhs  (shared for ux and uy)
            A_u = pr["G"] * (-L)   # -L is positive definite (Dirichlet)

            self._T_solve.append(factorized(csr_matrix(A_T)))
            self._p_solve.append(factorized(csr_matrix(A_p)))
            self._u_solve.append(factorized(csr_matrix(A_u)))

    # ------------------------------------------------------------------
    # Interior Laplacian (Dirichlet BC, finite differences)
    # ------------------------------------------------------------------

    def _build_interior_laplacian(self) -> csr_matrix:
        """
        Build the 2-D discrete Laplacian restricted to interior nodes.

        Uses a standard 5-point stencil with homogeneous Dirichlet BCs.
        Returns a negative-semidefinite CSR matrix of size [N_int, N_int].
        The operator satisfies  L u = ∇²u  (with ∇²u < 0 for a bump).
        """
        n_int = self.n_int
        h     = self.dx
        e     = np.ones(n_int)
        # 1-D second-derivative with Dirichlet BC (size n_int × n_int)
        D1 = diags([e[:-1], -2*e, e[:-1]], [-1, 0, 1], format="csr") / h**2
        I  = eye(n_int, format="csr")
        # 2-D: ∂²/∂x² + ∂²/∂y²  (x = column direction, y = row direction)
        L  = kron(I, D1, format="csr") + kron(D1, I, format="csr")
        return L

    # ------------------------------------------------------------------
    # Helpers: interior ↔ full grid
    # ------------------------------------------------------------------

    def _to_interior(self, field: np.ndarray) -> np.ndarray:
        """Extract interior nodes from full [size, size] field → [N_int] vector."""
        return field[1:-1, 1:-1].flatten()

    def _to_full(self, interior: np.ndarray) -> np.ndarray:
        """Embed [N_int] interior vector into full [size, size] field (zeros on boundary)."""
        full = np.zeros((self.size, self.size))
        full[1:-1, 1:-1] = interior.reshape(self.n_int, self.n_int)
        return full

    # ------------------------------------------------------------------
    # Gradient (central differences on full grid)
    # ------------------------------------------------------------------

    def _gradient(self, F: np.ndarray):
        """
        Compute ∂F/∂x and ∂F/∂y on the full [size, size] grid.
        Uses central differences in the interior, one-sided at boundaries.
        """
        h    = self.dx
        dFdx = np.zeros_like(F)
        dFdy = np.zeros_like(F)
        # Central differences (interior columns / rows)
        dFdx[:, 1:-1] = (F[:, 2:] - F[:, :-2]) / (2 * h)
        dFdy[1:-1, :] = (F[2:, :] - F[:-2, :]) / (2 * h)
        # One-sided at boundaries
        dFdx[:, 0]  = (F[:, 1]  - F[:, 0])  / h
        dFdx[:, -1] = (F[:, -1] - F[:, -2]) / h
        dFdy[0,  :] = (F[1,  :] - F[0,  :]) / h
        dFdy[-1, :] = (F[-1, :] - F[-2, :]) / h
        return dFdx, dFdy

    # ------------------------------------------------------------------
    # Random initial conditions (smooth Gaussian random field)
    # ------------------------------------------------------------------

    def _random_smooth_field(self, seed: int, scale: float = 1.0) -> np.ndarray:
        """
        Generate a smooth random field on the full [size, size] grid.

        Uses FFT with a low-pass Gaussian power spectrum.
        The field is zero on the boundary (Dirichlet).
        """
        rng = np.random.default_rng(seed)
        n   = self.size
        l   = rng.uniform(0.15, 0.40)   # spatial correlation length
        kx  = np.fft.fftfreq(n, d=1.0 / n)
        ky  = np.fft.fftfreq(n, d=1.0 / n)
        KX, KY = np.meshgrid(kx, ky, indexing="ij")
        psd    = np.exp(-0.5 * (KX**2 + KY**2) * l**2)
        noise  = rng.standard_normal((n, n)) + 1j * rng.standard_normal((n, n))
        field  = np.real(np.fft.ifft2(noise * np.sqrt(psd * n**2)))
        field  = (field - field.mean()) / (field.std() + 1e-8)
        # Apply Dirichlet mask (taper to zero at boundaries)
        x = np.linspace(0, np.pi, n)
        mask = np.outer(np.sin(x), np.sin(x))   # sin-window: zero on boundary
        return (scale * field * mask).astype(np.float64)

    def _get_init_cond(self, traj_index: int) -> tuple:
        """Return (T0, p0), each [size, size], zero on boundary."""
        seed = traj_index if not self.test else self.max_seed - traj_index
        T0   = self._random_smooth_field(seed,         scale=0.5)
        p0   = self._random_smooth_field(seed + 10000, scale=0.3)
        return T0, p0

    # ------------------------------------------------------------------
    # Heat source (fixed per environment: Gaussian hot-spot)
    # ------------------------------------------------------------------

    def _heat_source(self, env: int) -> np.ndarray:
        """
        Fixed spatial heat source Q_T(x,y) for environment `env`.
        Returns full-grid array [size, size].
        """
        n   = self.size
        x   = np.linspace(0, 1, n)
        X, Y = np.meshgrid(x, x, indexing="ij")
        # Hot-spot location: vary with environment
        x0  = 0.3 + 0.2 * env
        y0  = 0.5
        sigma = 0.10
        Q   = 0.5 * np.exp(-((X - x0)**2 + (Y - y0)**2) / (2 * sigma**2))
        return Q.astype(np.float64)

    # ------------------------------------------------------------------
    # One-step operator splitting
    # ------------------------------------------------------------------

    def _step(
        self,
        T: np.ndarray, p: np.ndarray,
        ux: np.ndarray, uy: np.ndarray,
        ev: np.ndarray,
        Q_T: np.ndarray, env: int,
    ):
        """
        Advance (T, p, ux, uy, ev) by one macro step dt_eval.

        Operator-splitting sequence:
          (1) Advance T  → backward Euler
          (2) Advance p  → backward Euler with thermal source from ΔT
          (3) Compute ev → algebraic from updated p and T
          (4) Solve ux, uy → two Poisson problems driven by ∇εv
        """
        pr = self.params_eq[env]
        dt = self.dt_eval

        # ---- (1) Heat equation: (ρc/dt I - κ L) T_new = (ρc/dt) T_old + Q_T ----
        rhs_T  = pr["rho_c"] / dt * self._to_interior(T) + self._to_interior(Q_T)
        T_int_new = self._T_solve[env](rhs_T)
        T_new  = self._to_full(T_int_new)

        # ---- (2) Hydraulic: (Ss/dt I - c_h L) p_new = (Ss/dt) p_old + L_T ΔT/dt ----
        dT_int  = T_int_new - self._to_interior(T)
        rhs_p   = pr["Ss"] / dt * self._to_interior(p) + pr["L_T"] / dt * dT_int
        p_int_new = self._p_solve[env](rhs_p)
        p_new  = self._to_full(p_int_new)

        # ---- (3) Volumetric strain (algebraic, simplified drained equilibrium) ----
        ev_new = (pr["alpha"] * p_new + pr["E_T"] * T_new) / pr["K_drained"]

        # ---- (4) Displacement: G ∇²ux = G ∂εv/∂x,  G ∇²uy = G ∂εv/∂y ----
        dEvdx, dEvdy = self._gradient(ev_new)
        rhs_ux  = pr["G"] * self._to_interior(dEvdx)
        rhs_uy  = pr["G"] * self._to_interior(dEvdy)
        ux_new  = self._to_full(self._u_solve[env](rhs_ux))
        uy_new  = self._to_full(self._u_solve[env](rhs_uy))

        return T_new, p_new, ux_new, uy_new, ev_new

    # ------------------------------------------------------------------
    # Simulation loop
    # ------------------------------------------------------------------

    def _simulate(self, env: int, traj_idx: int) -> np.ndarray:
        """
        Run the THM simulation for one trajectory.

        Returns:
            state: float32 array [5, n_steps, size, size]
                   channels: T, p, ux, uy, ev  (each normalised independently)
        """
        T0, p0 = self._get_init_cond(traj_idx)
        Q_T    = self._heat_source(env)

        T  = T0.copy()
        p  = p0.copy()
        ux = np.zeros((self.size, self.size))
        uy = np.zeros((self.size, self.size))
        pr = self.params_eq[env]
        ev = (pr["alpha"] * p + pr["E_T"] * T) / pr["K_drained"]

        T_snaps, p_snaps, ux_snaps, uy_snaps, ev_snaps = [], [], [], [], []

        def _snap():
            T_snaps.append(T.copy())
            p_snaps.append(p.copy())
            ux_snaps.append(ux.copy())
            uy_snaps.append(uy.copy())
            ev_snaps.append(ev.copy())

        _snap()   # t = 0 snapshot

        for _ in range(1, self.n_steps):
            T, p, ux, uy, ev = self._step(T, p, ux, uy, ev, Q_T, env)
            _snap()

        def _norm(arr_list):
            """Stack and normalise each channel to [0,1] globally."""
            arr  = np.stack(arr_list, axis=0)  # [T, n, n]
            vmin = arr.min()
            vmax = arr.max()
            return (arr - vmin) / (vmax - vmin + 1e-10)

        T_arr  = _norm(T_snaps)
        p_arr  = _norm(p_snaps)
        ux_arr = _norm(ux_snaps)
        uy_arr = _norm(uy_snaps)
        ev_arr = _norm(ev_snaps)

        state = np.stack([T_arr, p_arr, ux_arr, uy_arr, ev_arr], axis=0).astype(np.float32)
        return state  # [5, T, n, n]

    # ------------------------------------------------------------------
    # Dataset interface
    # ------------------------------------------------------------------

    def __len__(self) -> int:
        return self.len

    def __getitem__(self, index: int) -> dict:
        env      = index // self.num_traj_per_env
        traj_idx = index % self.num_traj_per_env

        if index not in self.buffer:
            self.buffer[index] = self._simulate(env, traj_idx)

        state = torch.from_numpy(self.buffer[index])   # [5, T, n, n]
        t     = torch.arange(self.n_steps, dtype=torch.float32) * self.dt_eval
        return {"state": state, "t": t, "env": env}


# ------------------------------------------------------------------
# Default parameter sets (exported for use in gen_data.py)
# ------------------------------------------------------------------

def default_thm_params() -> list:
    """Three THM environments with increasing stiffness / decreasing permeability."""
    return THMDataset.DEFAULT_PARAMS
